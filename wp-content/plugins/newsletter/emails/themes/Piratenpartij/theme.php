															<p style="font-family: verdana,sans-serif; color: #333333; font-size: 20px; line-height: 22px; margin: 0; padding: 0;"><strong><a style="color: #572B85; text-decoration: none;" name="ict">Werkgroep: ICT</a></strong></p>
															<p>De werkgroep ICT heeft een bewogen tijd achter de rug en zit midden in een aantal grote projecten. Daarom zetten we hun werk deze maand extra in het zonnetje.</p>
															<p><strong>Doorstart van de werkgroep</strong><br />Enkele betrokkenen bij de werkgroep ICT hebben in het begin van dit jaar een plan van aanpak geschreven op verzoek van de werkgroep 'Organisatie'. Dit plan is aan het bestuur aangeboden, dat haar steun hiervoor uitgesproken heeft.</p>
															<p>Met ingang van april is de doorstart van de werkgroep ICT een feit. Er zijn nieuwe servers bij gekomen en momenteel wordt er druk gewerkt om hier al onze diensten naar over te zetten. Daarnaast wordt er voortdurend overlegd om onze interne processen te stroomlijnen.</p>
															<p>Ben je een techneut en wil je weten hoe de Piratenpartij haar servers inricht? De meeste van onze configuraties zijn openbaar inzichtelijk en natuurlijk vrij over te nemen! Kijk daarvoor op onze <a href="https://github.com/Piratenpartij/systems">Github</a> en stuur een 'pull request' als je zelf verbeteride&euml;en hebt!</p>
															<p><strong>Heartbleed</strong><br />Onze systeembeheerders zijn tot diep in de nacht in de weer geweest naar aanleiding van een <a href="http://heartbleed.com">bug in OpenSSL</a>, dat voor de beveiligde https-verbinding met onze website moet zorgen. De certificaatautoriteit StartCom, waar wij een betaalde overeenkomst mee hadden, eiste een <a href="http://www.techdirt.com/articles/20140409/11442426859/shameful-security-startcom-charges-people-to-revoke-ssl-certs-vulnerable-to-heartbleed.shtml">grote som geld</a> om de potentieel gelekte certificaten in te trekken.
															<p>Onze website heeft om deze reden enige tijd met <a href="https://en.wikipedia.org/wiki/Self-signed_certificate">'self signed' certificaten</a> gedraaid. Deze zijn wel veilig, maar worden door de meeste browsers niet als 'vertrouwd' gezien. Hierdoor kregen onze bezoekers waarschuwingen van hun browser, wat voor enige onrust zorgde. Inmiddels is dit opgelost en hebben we bijna overal weer een 'vertrouwd' certificaat, ditmaal van <a href="https://www.geotrust.com">GeoTrust</a>.
															<p><strong>Website</strong><br />Het kan je bijna niet ontgaan zijn. De Piratenpartij heeft een <a href="https://piratenpartij.nl">gloednieuwe website</a>! Deze is tegelijk met de presentatie van de kandidaten en het partijprogramma gelanceerd, ondanks de problemen met onze certificaten. De nieuwe site heeft een frisse en schone uitstraling en hoewel het voor alle vrijwilligers - ook bij ICT - nog wel even wennen is,  zijn we erg blij met deze nieuwe website de EP2014 campagne in te kunnen.  Uiteraard kun je de vrije en opensource broncode van de website ook inzien op <a href="https://github.com/Piratenpartij/piratenpartij.nl">Github</a>.
															<p><strong>Toekomstplannen</strong><br/>In mei blijft de migratie naar de nieuwe servers prioriteit, zodat iedereen daarna in een gestructureerde werkomgeving aan de slag kan. Daarna willen we onder andere de layout van onze subsites, zoals het forum, onder handen gaan nemen. Daar kunnen we natuurlijk wel wat hulp bij gebruiken. Stuur alvast een ori&euml;nterend mailtje naar <a href="mailto:patrick.godschalk@piratenpartij.nl">patrick.godschalk@piratenpartij.nl</a> voor meer informatie over wat er te doen is en houd <a href="https://piratenpartij.nl/meedoen">de website</a> in de gaten voor concrete vacatures.</p>
